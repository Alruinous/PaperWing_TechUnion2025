import datetime
import json
import os
from typing import Dict, List, Optional
import arxiv
import asyncio

class ArxivScraper:
    def __init__(self, data_file: str = "arxiv_daily.json"):
        self.data_file = data_file
        self.client = arxiv.Client()
        self.ensure_data_file()

    def ensure_data_file(self):
        """确保数据文件存在"""
        if not os.path.exists(self.data_file):
            with open(self.data_file, "w") as f:
                json.dump({}, f)

    @staticmethod
    def format_authors(authors: List[any]) -> str:
        """格式化作者列表为逗号分隔的字符串"""
        return ", ".join(author.name for author in authors) if authors else ""
    
    @staticmethod
    def extract_year_from_date(date: datetime.date) -> Optional[int]:
        """从日期中提取年份"""
        return date.year if date else None

    async def fetch_papers(self, query: str, max_results: int = 10) -> List[Dict]:
        """
        获取论文数据
        :param query: 搜索查询
        :param max_results: 最大结果数量
        :return: 论文数据列表
        """
        papers = []
        
        search = arxiv.Search(
            query=query,
            max_results=max_results,
            sort_by=arxiv.SortCriterion.SubmittedDate,
            sort_order=arxiv.SortOrder.Descending
        )

        for result in self.client.results(search):
            paper_data = {
                'title': result.title,
                'authors': self.format_authors(result.authors),
                'journal': getattr(result, 'journal_ref', None) or "arXiv",
                'year': self.extract_year_from_date(result.published.date()),
                'abstract': result.summary,
                'keywords': getattr(result, 'primary_category', None),
                'source': 'arxiv',
                'research_fields': ", ".join(getattr(result, 'categories', [])),
                'arxiv_id': result.get_short_id(),
                'pdf_url': next((link.href for link in result.links if link.title == 'pdf'), None),
                'published_date': result.published.date().isoformat(),
                'updated_date': result.updated.date().isoformat() if hasattr(result, 'updated') else None,
                'comments': getattr(result, 'comment', "") or ""
            }
            papers.append(paper_data)
        
        return papers

    def update_data(self, new_data: Dict[str, List[Dict]]):
        """更新存储的论文数据"""
        try:
            with open(self.data_file, "r") as f:
                existing_data = json.load(f)
        except (json.JSONDecodeError, FileNotFoundError):
            existing_data = {}

        # 合并新数据
        for topic, papers in new_data.items():
            if topic in existing_data:
                existing_data[topic].extend(papers)
            else:
                existing_data[topic] = papers

        with open(self.data_file, "w") as f:
            json.dump(existing_data, f, indent=2)

    def generate_markdown(self, output_file: str = "README.md"):
        """生成Markdown格式的报告"""
        current_date = datetime.date.today().strftime("%Y.%m.%d")
        
        try:
            with open(self.data_file, "r") as f:
                data = json.load(f)
        except (FileNotFoundError, json.JSONDecodeError):
            data = {}

        with open(output_file, "w+") as f:
            f.write(f"## Updated on {current_date}\n\n")
            
            for topic, papers in data.items():
                if not papers:
                    continue
                    
                f.write(f"## {topic}\n\n")
                for paper in papers:
                    f.write(f"### {paper['title']}\n")
                    f.write(f"- **Authors**: {paper['authors']}\n")
                    f.write(f"- **Published**: {paper['published_date']}\n")
                    f.write(f"- **PDF**: [Link]({paper['pdf_url']})\n")
                    f.write(f"- **Abstract**: {paper['abstract'][:200]}...\n\n")

