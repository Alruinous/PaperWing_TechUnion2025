    async def run(self, max_results: int = 5):
        """执行每日抓取任务"""
        collected_data = {}
        
        for topic, query in self.topics.items():
            print(f"Fetching papers for: {topic}")
            try:
                papers = await self.scraper.fetch_papers(query, max_results)
                print(topic, query)
                collected_data[topic] = papers
                print(f"Found {len(papers)} papers\n")
            except Exception as e:
                print(f"Error fetching {topic}: {str(e)}\n")

        # 更新数据并生成报告
        if collected_data:
            self.scraper.update_data(collected_data)
            self.scraper.generate_markdown()
            print("Daily update completed successfully")
        else:
            print("No new papers collected")

        return collected_data

